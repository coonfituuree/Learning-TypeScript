/* Syntax: let myVar: type = value */

//For String
let myName: string = "Galym";
console.log(myName);

myName = "Another Name";
console.log(myName);

//For Number
let favNumber: number = 69;
favNumber += 2;
console.log(favNumber);

//For Boolean
let isHard: boolean = false;
isHard = true;
console.log(isHard);



